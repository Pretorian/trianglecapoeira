<h4>Do I need to be fit?</h4>
<p>No you will gain all round fitness training with us, and we will help you build your fitness.</p>
<h4>Do I need experience?</h4>
<p>Most of our students today started with no experience, there is also no need to have martial arts or gymnastic experience.You will gradually build strength flexibility and technique as you work through your belts.</p>
<h4>Are all you classes good for beginners?</h4>
<p>Our classes are a general level and we divide the classes into levels, there is always somebody to help you along if you can't make the beginners class.</p>

<h4>Can I pay monthly?</h4>
<p>No, at the moment you pay for each individual class that you do.  You can pay in cash or credit card, $15 USD per class.</p>
<h4>Do I need to book in advance?</h4>
<p>No just turn up, although the classes at Balanced Movement have a limit for the amount of students in the class; so it's best not to turn up last minute as you may not get in.</p>
<h4>Do I need a uniform?</h4>
<p>You will only need a uniform when you decide you want to train Capoeira on a regular Basis.</p>
<h4>What shall I wear in the beginning?</h4>
<p>Wear loose light clothing such as tracksuit bottoms and a thin t-shirt/vest top, it gets hot!</p>
<h4>What do I wear on my feet?</h4>
<p>Bare feet is the best option, you may need shoes in the beginning but no shoes is better until your feet toughen up. We advise you not to wear shoes/trainers that grip the floor too much, your feet are awesome already.  Ask your teacher about footwear when you decide to take it up seriously.</p>
<h4>Do I need to be able to speak portuguese?</h4>
<p>You will pick up alot of the language from learning the movements, songs, and hearing people talking, most of our students first language is English and many have learned portuguese within the group and extra studies.</p>
<h4>Do you play Capoeira Angola or Regional?</h4>
<p>Mestre Saussuna has during his long career had influence over many Mestres and capoeiristas from all sides of the capoeira spectrum including Angola and Regional. In the course of the generations we have seen his influence range from the very playful capoeirista to the very cunning capoeirista. When asked what we are he has often said 'you are what the music asks of you'. (Mestre Saussuna)</p>
